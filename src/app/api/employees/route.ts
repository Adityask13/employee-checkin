import { NextRequest, NextResponse } from 'next/server';

const AWS_EMPLOYEE_API_URL = 'https://0uq172mu3k.execute-api.us-east-2.amazonaws.com/default/EmployeeLambdaHandler';

// Handle CORS preflight requests
export async function OPTIONS() {
  return new Response(null, {
    status: 200,
    headers: {
      'Access-Control-Allow-Origin': '*',
      'Access-Control-Allow-Methods': 'GET, POST, PUT, DELETE, OPTIONS',
      'Access-Control-Allow-Headers': 'Content-Type, Authorization, X-Requested-With',
      'Access-Control-Max-Age': '86400',
    },
  });
}

export async function GET(request: NextRequest) {
  try {
    const { searchParams } = new URL(request.url);
    const employeeId = searchParams.get('employee_id');
    
    // Build query string if employee_id is provided
    const queryString = employeeId ? `?employee_id=${employeeId}` : '';
    
    const response = await fetch(`${AWS_EMPLOYEE_API_URL}${queryString}`, {
      method: 'GET',
      headers: {
        'Content-Type': 'application/json',
        'Accept': 'application/json',
      },
    });

    if (!response.ok) {
      const errorText = await response.text();
      console.error('AWS Employee API error:', response.status, errorText);
      return NextResponse.json(
        { error: `Employee API error: ${response.status}` },
        { status: response.status }
      );
    }

    const data = await response.json();
    return NextResponse.json(data);
  } catch (error) {
    console.error('Employee API proxy error:', error);
    return NextResponse.json(
      { error: 'Internal server error' },
      { status: 500 }
    );
  }
}

